function isLauncherSkinFind(model)
	if model > 311 then
		if Skins[model] then
			return Skins[model]
		end

		return Ped
	end
	return model
end

function isLauncherVehicleFind(model)
	if model > 611 then
		if Cars[model] then
			return Cars[model]
		end

		return Car
	end
	return model
end

function sampev.onSendClientJoin(ver, mod, nick, response, authkey, clientver, unk)
	return {ver, mod, nick, response, authkey, "Arizona PC", unk}
end

function sampev.onShowTextDraw(id, data)
	if Cars[data.modelId] then
		data.modelId = Cars[data.modelId]
	elseif Skins[data.modelId] then
		data.modelId = Skins[data.modelId]
	end

	return {id, data}
end

function sampev.onPlayerStreamIn(id, team, model, pos, rot, color, fStyle)
	model = isLauncherSkinFind(model)
	return {id, team, model, pos, rot, color, fStyle}
end

function sampev.onSetPlayerSkin(id, model)
	model = isLauncherSkinFind(model)
	return {id, model}
end

function sampev.onCreateActor(id, model, pos, rot, health)
	model = isLauncherSkinFind(model)
	return {id, model, pos, rot, health}
end

function sampev.onVehicleStreamIn(id, data)
	data.type = isLauncherVehicleFind(data.type)
	return {id, data}
end
